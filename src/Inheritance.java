public class Inheritance {
    String car;
    public void carName(String car)
        {
            this.car = car;
            System.out.println("Car Name is: "+car);
        }
        public void modelName(String model)
        {
            System.out.println("Model: "+model);
        }
        public void yearMethod(int year)
        {
            System.out.println("Year: "+year);
        }

}
